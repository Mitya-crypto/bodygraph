import { NextRequest, NextResponse } from 'next/server'

export async function POST(request: NextRequest) {
  let body: any; // –û–±—ä—è–≤–ª—è–µ–º body –∑–¥–µ—Å—å
  try {
    body = await request.json()
    const { message, context, isPremium, userProfile } = body

    console.log('ü§ñ ChatGPT request:', {
      message: message.substring(0, 100) + '...',
      isPremium,
      hasProfile: !!userProfile,
      hasApiKey: !!process.env.OPENAI_API_KEY,
    })

    // –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–æ–º—Ç –¥–ª—è ChatGPT
    const systemPrompt = isPremium 
      ? getPremiumSystemPrompt(userProfile, context)
      : getBasicSystemPrompt(userProfile, context)

    const messages = [
      {
        role: 'system',
        content: systemPrompt
      },
      {
        role: 'user',
        content: message
      }
    ]

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ API –∫–ª—é—á–∞
    if (!process.env.OPENAI_API_KEY) {
      throw new Error('OpenAI API key not configured')
    }

    // –í—ã–∑—ã–≤–∞–µ–º ChatGPT API
    const openaiResponse = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${process.env.OPENAI_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: isPremium ? 'gpt-4' : 'gpt-3.5-turbo',
        messages,
        max_tokens: isPremium ? 1000 : 500,
        temperature: 0.7,
        presence_penalty: 0.1,
        frequency_penalty: 0.1,
      }),
    })

    if (!openaiResponse.ok) {
      throw new Error(`OpenAI API error: ${openaiResponse.status}`)
    }

    const data = await openaiResponse.json()
    const response = data.choices[0]?.message?.content || '–ò–∑–≤–∏–Ω–∏—Ç–µ, –Ω–µ –º–æ–≥—É –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –≤–∞—à –∑–∞–ø—Ä–æ—Å.'

    console.log('‚úÖ ChatGPT response generated')

    return NextResponse.json({
      success: true,
      response,
      model: isPremium ? 'gpt-4' : 'gpt-3.5-turbo',
      isPremium,
    })

  } catch (error) {
    console.error('‚ùå ChatGPT error:', error)
    
    // Fallback response –µ—Å–ª–∏ API –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω
    const fallbackResponse = generateFallbackResponse(
      body?.message || '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π –∑–∞–ø—Ä–æ—Å', 
      body?.isPremium || false
    )
    
    return NextResponse.json({
      success: false,
      response: fallbackResponse,
      error: error instanceof Error ? error.message : 'Unknown error',
      isFallback: true,
    })
  }
}

